package com.example.tns;

public class Loja {

    String nomeLoja;
    Integer idLoja;
    Integer idEmpresaRF;
    String telefoneLoja;
    String cepLoja;
    String enderecoLoja;
    String emailLoja;

    public String getNomeLoja() {
        return nomeLoja;
    }

    public void setNomeLoja(String nomeLoja) {
        this.nomeLoja = nomeLoja;
    }

    public Integer getIdLoja() {
        return idLoja;
    }

    public void setIdLoja(Integer idLoja) {
        this.idLoja = idLoja;
    }

    public Integer getIdEmpresaRF() {
        return idEmpresaRF;
    }

    public void setIdEmpresaRF(Integer idEmpresaRF) {
        this.idEmpresaRF = idEmpresaRF;
    }

    public String getTelefoneLoja() {
        return telefoneLoja;
    }

    public void setTelefoneLoja(String telefoneLoja) {
        this.telefoneLoja = telefoneLoja;
    }

    public String getCepLoja() {
        return cepLoja;
    }

    public void setCepLoja(String cepLoja) {
        this.cepLoja = cepLoja;
    }

    public String getEnderecoLoja() {
        return enderecoLoja;
    }

    public void setEnderecoLoja(String enderecoLoja) {
        this.enderecoLoja = enderecoLoja;
    }

    public String getEmailLoja() {
        return emailLoja;
    }

    public void setEmailLoja(String emailLoja) {
        this.emailLoja = emailLoja;
    }
}
